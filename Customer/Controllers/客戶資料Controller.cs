using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;

namespace Customer.Models
{
    public class 客戶資料Controller : Controller
    {
        private CustomerEntities db = new CustomerEntities();

        // GET: 客戶資料
        
        public ActionResult Index(string txtName, string txtNo)
        {
                        
            string sql= "select * from 客戶資料 where 是否已刪除=0 ";
           


            if (!String.IsNullOrEmpty(txtName))
            {
                sql += "And 客戶名稱 like +'%'+@p0+'%'";
            }

            if (!String.IsNullOrEmpty(txtNo))
            {
                sql += "And 統一編號 like +'%'+@p1+'%'";
            }

            var data = db.Database.SqlQuery<客戶資料>(sql,txtName, txtNo);
            return View(data);
            //return View(db.客戶資料.ToList());
        }

        //查詢客戶名稱、聯絡人數量、銀行帳戶數量
        public ActionResult vwCustomer()
        {
            var data = db.vwCustomer.ToList();
            return View(data);
        }

        // GET: 客戶資料/Details/5
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            客戶資料 客戶資料 = db.客戶資料.Find(id);
            if (客戶資料 == null)
            {
                return HttpNotFound();
            }
            return View(客戶資料);
        }

        // GET: 客戶資料/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: 客戶資料/Create
